//add click for love
let love = document.querySelectorAll(".love");
let episode_clone = Array.from(document.querySelectorAll(".episodeclone"));
love.forEach((element) => {
  element.addEventListener("click", (ex) => {
    //add class to the element i clicked on
    ex.currentTarget.classList.toggle("add-to-love-side");
    //check on the class,if it contain add to love it will pushed to array and print some message
    if (ex.currentTarget.classList.contains("add-to-love-side")) {
      Swal.fire({
        icon: "success",
        title: "تمت إضافة الحلقة بنجاح",
        confirmButtonText: "حسنا",
        background: "#151c24",
        timer: 2000,
      });
      //check the element in the array or not if it there will not added
      for (let i = 0; i <= episode_clone.length; i++) {
        const ele = ex.currentTarget.parentElement.parentElement;
        if (episode_clone.includes(ele)) {
        } else {
          episode_clone.push(ex.currentTarget.parentElement.parentElement);
          let element = ex.currentTarget.parentElement.parentElement;
          for (let ele = 0; ele < 1; ele++) {
            //cloned father
            let episodeclone = document.createElement("div");
            episodeclone.classList.add("episodeclone");
            //cloned info div=======================================
            let info = document.createElement("div");
            info.classList.add("info");
            //cloned img div=======================================
            let episodecloneimg = document.createElement("div");
            episodecloneimg.classList.add("episodecloneimg");
            //cloned text div=======================================
            let episodeclonetext = document.createElement("div");
            episodeclonetext.classList.add("episodeclonetext");
            //cloned text div h=======================================
            let episodeclonetexth = document.createElement("h3");
            //cloned btn div=======================================
            let btn_watch_del = document.createElement("div");
            btn_watch_del.classList.add("btn_watch_del");
            //cloned button_watch=======================================
            let del_button = document.createElement("button");
            del_button.innerHTML = "delete";
            del_button.classList.add("del_button");
            //cloned button_watch=======================================
            let watch_button = document.createElement("button");
            watch_button.innerHTML = "watch";
            watch_button.classList.add("watch_button");
            //father of episode
            //img of episode
            let img = element
              .getElementsByTagName("img")[0]
              .getAttribute("src");
            let image = document.createElement("img");
            image.setAttribute("src", img);
            //tittle of episode
            let title =
              element.getElementsByClassName("episode-name")[0].textContent;
            //appending++++++++++++++++
            episodeclone.appendChild(info);
            info.appendChild(episodecloneimg);
            episodecloneimg.appendChild(image);
            info.appendChild(episodeclonetext);
            episodeclonetext.appendChild(episodeclonetexth);
            episodeclonetexth.innerHTML += title;
            episodeclone.appendChild(btn_watch_del);
            btn_watch_del.appendChild(del_button);
            btn_watch_del.appendChild(watch_button);
            let loved_video = document.querySelector(".loved-video");
            loved_video.appendChild(episodeclone);
            //function for delete button
            let del_buttons = document.querySelectorAll(".del_button");
            let watch_buttons = document.querySelectorAll(".watch_button");
            del_buttons.forEach((element) => {
              element.addEventListener("click", function () {
                element.parentElement.parentElement.remove();
              });
            });
          }
        }
      }
      //check on the class,if it not contain add to love it will removed from array and print some message
    } else if (ex.currentTarget.className === "love") {
      Swal.fire({
        icon: "info",
        text: "تم حذف الحلقة بنجاح يرجي حذفها من هنا أيضا",
        background: "#151c24",
        confirmButtonText: "حذف الآن",
      }).then((result) => {
        /* Read more about isConfirmed, isDenied below */
        if (result.isConfirmed) {
          let overlay = document.querySelector(".overlay");
          overlay.classList.add("tranlatex");
        }
      });
      let index = episode_clone.indexOf(ex.currentTarget);
      episode_clone.splice(index, 1);
    }
  });
});

